


// La estructura while es clasificada como estrutura de repeticion con teste logico de inicio. Si el teste logico es true continua el loop, si es false para.

var contador1 = 1
while (contador1 <= 6) {
    console.log(`Passo ${contador1}`)
    contador1++ // c = c + 1
}



// Ejecuta el bloque y despues hace el teste. Estructura de repeticion con teste logico no final

var contador2 = 1
do {
    console.log(`cont ${contador2}`)
    contador2++
} while (contador2 <= 6)

